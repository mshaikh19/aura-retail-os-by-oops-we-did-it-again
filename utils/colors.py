class Colors:
    """
    ANSI color codes for terminal UI styling.
    """
    HEADER = '\033[95m'    # Magenta/Purple
    BLUE = '\033[94m'      # Borders
    CYAN = '\033[96m'      # Info
    SUCCESS = '\033[92m'   # Green
    ERROR = '\033[91m'     # Red
    WARNING = '\033[93m'   # Yellow
    TEXT = '\033[97m'      # White
    DIM = '\033[2m'        # Faint text
    RESET = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
